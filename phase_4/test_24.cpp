void solve(){
    lli n,m,q;
    cin >> n >> m >> q;
    lli temp = __gcd(n,m);       // dual wall will create when gcd became
                                 // outer and inner wall will meet

    lli a1 = n/temp;   // 4/2 = 2     // meet at 2-inner and 3-outer
    lli a2 = m/temp;   // 6/2 = 3

    while(q--){
        lli a,b,c,d;
        cin  >> a >> b >> c >> d;
//              2    6    2    4

        lli p1,p2;
        if(a==1) p1 = ceil( (ld)b/a1 ); 
        else p1 = ceil( (ld)b/a2 );

        if(c==1) p2 = ceil( (ld)d/a1 );
        else p2 = ceil( (ld)d/a2 );

        cout << p1 << ' ' << p2 << endl;
        if(p1 == p2) cout  << "YES" << endl;      // when noth points in same group
        else cout << "NO" << endl;

    }




}




