void solve(){
    lli a,b;
    cin >> a >> b;

    lli ans=0;
    for(int i=1 ;i<=60 ;i++ ){
        lli temp = (1LL<<i) - 1;                   //           1  11  111  1111 11111
        for(int j=0 ;j<=i-2 ;j++ ){                // shift :   1   2   3    4    5
            lli cur = temp - (1LL<<j);             // for ,1111 we can - 1(1) ,10(3) ,100()
            if( cur>=a and cur<=b ) ans++;
        }
    }
    cout << ans%mod << endl;      //   111111   shift:6
}                                 //   - 1 2 4 8 16
                                  //    1 10 100 1000 10000   // last shift : 4 = 6-2



