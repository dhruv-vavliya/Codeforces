bool ok( lli k ,lli n ,lli a ,lli b ,lli pos ,lli ans ){
    if( ans > pos ) return false;     // ans : max a can be used
    n -= ans;
    k -= ans*a;

    lli mb=0;
    if(b<k) mb= (k - b)/b;
    if( b<k and (k-b)%b ) mb++;

    // cout << "pos " << ans << endl;
    return mb >= n;
}



void solve(){

    lli k,n,a,b;
    cin >> k >> n >> a  >> b;

    lli pos = 0;
    if(k>a) pos = (k-a)/a;
    if( k>a and (k-a)%a ) pos++;

    lli l=0 ,r=n ,ans=-1;
    while( l<=r ){
        lli mid = l + (r-l)/2;
        if( ok(k,n,a,b,pos,mid) ){
            ans = mid;
            l = mid+1;
        }
        else r = mid-1;
    }

    cout << ans << endl;




}




