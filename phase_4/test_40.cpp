    // cout << s << endl;

    lli i=1;
    while(i<=n){
        lli sm=0;
        if(s[i] == '1'){
            lli pre = i;
            while( i<=n and s[i]!='0' ) sm+=a[i] ,i++;
            if(i<=n) sm+=a[i];
            lli req = i*(i+1)/2 - (pre-1)*pre/2;
            if( sm != req ){cout << "NO" <<  endl; return; }
        }
        else{
            if( a[i]!=i ){ cout << "NO" << endl; return; }
        }
        i++;
    }   
