void solve(){
    int n;
    cin >> n ;

    string s;
    cin >> s;
    s = 'd' + s;

    int d=0 ,k=0;
    int ans[n+1];


    map< pair<int,int> ,int> mp;
    for(int i=1 ;i<=n ;i++){
        ans[i] = 1;
        if( s[i] == 'D' ) d++;
        else k++;

        int g = __gcd( d,k );
        int a = d/g;
        int b = k/g;
        mp[{a,b}]++;
        cout << mp[{a,b}] << ' ';
    }
    cout << '\n';
}




