void solve(){
    int n;
    cin >> n ;

    map<int,int> mp;
    vector<pair<int,int>> v( n*(n-1)/2 );
    for(int i=0 ;i<n*(n-1)/2-1 ;i++){
        cin >> v[i].first;
        cin >> v[i].second;
        mp[v[i].first]++;
        mp[v[i].second]++;
    }

    vi ans;
    for(auto val : mp){
        if( val.second != n-1 ) ans.pb(val.first);
    }


    int t1=0,t2=0;
    for(int i=0 ;i<n*(n-1)/2-1 ;i++ ){
        if( v[i].first == ans[0] ) t1++;
        if( v[i].first == ans[1] ) t2++;
    }
    
    if( t1>t2 ) cout << ans[0] << ' ' << ans[1] << endl;
    else cout << ans[1] << ' ' << ans[0] << endl;
    

}




