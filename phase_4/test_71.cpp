void solve(){
    int n;
    cin >> n;

    string s;
    cin >> s;

    int ans=1;
    int r=0,u=0,d=0,l=0;

    for(int i=0 ;i<n ;i++){
        if( (s[i] == 'D' and u>0) || (s[i] == 'U' and d>0) || (s[i] == 'L' and r>0) || (s[i] == 'R' and l>0) ){
            ans++;
            l=0 ,r=0 ,u=0 ,d=0;
        }

        if(s[i] == 'R') r++;
        if(s[i] == 'L') l++;
        if(s[i] == 'U') u++;
        if(s[i] == 'D') d++;
    }
    cout << ans << endl;








    

}




