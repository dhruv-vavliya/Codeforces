void solve(){

    lli n,k;
    cin >> n >> k;


    vi a(n+1);
    for(int i=1; i<=n ;i++) cin >> a[i];

    vi temp;
    lli ans=0 ,d=0;
    for(int i=1 ;i<=n ;i++){
        if( a[i]%10!=0 ) temp.pb( 10 - a[i]%10 );
        ans += a[i]/10;
        d += ceil( (double)a[i]/10 );
    }

    sort(all(temp));
    int i=0;
    while( i<temp.size() and  k-temp[i] >= 0 ){
        k -= temp[i];
        ans++;
        i++;
    }

    ans += min( k/10 ,10*n - d );
    cout << ans  << endl;
}




