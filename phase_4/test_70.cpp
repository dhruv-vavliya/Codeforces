void solve(){
    lli n,x,y;
    cin >> n >> x >>  y;

    string s;
    cin >> s;
    s = '@' + s;

    //    reverse :  x
    //    swap : y

    int i=1 ,j=n;
    while(i<=n and  s[i] == '1' ) i++;
    if(i>n){ cout << 0 << endl; return; }

    while(j>=1 and s[j] == '1') j--;   
    
    lli c=0;
    while( i<=j ){
        if( s[i] == '1' ){
            while( i<=j and s[i] == '1' ) i++;     // remove left-right ones and find-out (111)-groups
            c++;
        }
        i++;
    }

    lli ans = min( c*x + y , (c+1)*y );            // minimum of side-out all ones and then swap all 0 to 1
    cout << ans << endl;                           // or just swap 0 to 1



    

}




