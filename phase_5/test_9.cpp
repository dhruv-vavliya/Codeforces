void solve(){
    int n;
    cin >> n;

    string s;
    cin >> s;

    int o=0,z=0;
    for(int i=0 ;i<n ;i++){
        if(s[i] == '0') z++;
        else o++;
    }

    if( o&1 || z&1 || s[0] == '0' || s.back() == '0' ){ cout << "NO" << endl; return; }

    string a,b;
    for(int i=0 ;i<n ;i++){
        if( s[i] == '1' ){
            if(o) a += '(' ,b += '(' ,o-=2;
            else a += ')' ,b += ')';
        }
        else{
            if(z%2 == 0) a += '(' ,b += ')' ,z--;
            else a += ')' ,b += '(' ,z--;
        }
    }

    cout << "YES" << endl;
    cout << a  << '\n' << b << endl;

}




