void solve(){

    int n;
    string s;
    cin >> n >> s;

    vi p0 ,p1 ,ans(n+1);
    int t=0;

    for(int i=0 ;i<n ;i++){
        if( s[i] == '0' ){                      // subsequence ends  with 0.
            if( p1.empty() ){
                p0.pb(++t);
                ans[i+1] =t;
            }else{
                int temp = p1.back();
                p1.pop_back();
                p0.push_back( temp );
                ans[i+1] = temp;
            }
        }
        else{                                  // subsequence ends  with 1.
            if( p0.empty() ){
                p1.pb(++t);
                ans[i+1] = t;
            }else{
                int temp = p0.back();
                p0.pop_back();
                p1.push_back( temp );
                ans[i+1] = temp;
            }
        }
    }

    cout << t << endl;
    disp1(ans);
}




