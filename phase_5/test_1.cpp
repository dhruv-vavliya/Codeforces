void go( vi ans){
    for(int i=0 ;i<ans.size()-1 ;i++){
        cout << ans[i] << '.';
    }
    cout << ans.back()  << endl;
}



void solve(){
    vi ans;
    int n;
    cin >> n;

    int x;
    cin >> x;

    ans.pb(1);
    cout << 1 << endl;
    n--;

    while(n--){
        cin >> x;
        if(x == 1){
            ans.pb(1);
            go(ans);
            continue;
        }

        for( int i=ans.size()-1 ;i>=0 ;i-- ){
            if( ans[i]+1 == x ){
                ans[i] = x;
                go(ans);
                break; 
            }
            else ans.pop_back();
        }
    }

}




