void solve(){

    lli n,a,b,k;
    cin >> n >> a >> b >> k;

    vl ar(n+1);
    for(int i=1 ;i<=n ;i++) cin >> ar[i];

    // k times your choice that skip friend's turn
    int c=0 ,d=0;
    vl rem;
    for(int i=1 ;i<=n ;i++){
        lli temp = ar[i]%(a+b);
        if( ar[i] < a+b ) temp = ar[i];

        if( temp>0 and temp<=a ) c++;
        else{
            if(temp == 0) rem.pb( ceil( (ld)b/a ) );
            else temp-=a , rem.pb( ceil( (ld)temp/a ) );
        }
    }
    
    sort(all(rem));
    for( auto val : rem ){
        if( k - val < 0 ) break;
        k -= val;
        d++;
    }

    cout << c+d << endl;
}




