void declare(lli ans){
    cout << '!' << ans << endl;
    exit(0);
}



void solve(){

    lli n;
    cin >> n;

    lli pos = query(1,n);                   // position of second max
    if(query(1 ,pos) == pos ){
        lli l=1 ,r=pos-1;
        while( l<=r ){
            lli mid = (l+r)/2;
            if( query(mid ,pos) == pos ) l = mid+1;
            else r = mid-1;
        }
        declare(--l);
    }
    else{
        lli l = pos+1 ,r=n;
        while(l<=r){
            lli mid = (l+r)/2;
            if( query(pos ,mid) == pos ) r = mid-1;
            else  l = mid+1;
        }
        declare(++r);
    }
}




