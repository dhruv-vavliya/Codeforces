void solve(){
    int n,l,r,s;
    cin >> n >> l >> r >> s;

    vi vis(n+1 ,0);
    vi ans(n+1);
    int sm=0;

    int c=1;
    for(int i=l ;i<=r ;i++ ) ans[i] = c ,c++ ,sm += ans[i];
    if(sm > s){ cout << -1 << endl; return; }

    int possible = n;
    for(int i=r ;i>=l ;i--){
        sm -= ans[i];
        ans[i] = min(possible ,s -sm );
        sm += ans[i];
        vis[ans[i]] = 1;
        possible = ans[i]-1;
    }   

    if( sm!=s ){ cout << -1 << endl; return ;}
    c=1;
    for(int i=1 ;i<=n ;i++ ){
        if( i>=l and i<=r ){ continue; }
        while( vis[c] ) c++;
        ans[i] = c;
        c++;
    }

    for(int i=1 ;i<=n ;i++) cout << ans[i] << ' ';
    cout << endl;
}




