void solve(){
    int n,x,y,d;
    cin >> n >> x >> y >> d;

    if( abs(y-x)%d == 0 ) cout << abs(y-x)/d << endl;
    else{
        int ans= 1e12; 
        if( (y-1)%d == 0 )      ans = min( ans ,(x-1)/d + bool((x-1)%d !=0)  + (y-1)/d);
        if( (n-y)%d == 0 ) ans = min( ans ,(n-x)/d + bool((n-x)%d !=0)  + (n-y)/d );
        
        if( ans == INT_MAX ) cout << -1 << endl;
        else cout << ans << endl;
    }
}




