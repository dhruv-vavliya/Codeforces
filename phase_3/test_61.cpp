void solve(){
    lli n;
    cin >> n;

    vl a(n+1);
    for(int i=1 ;i<=n ;i++) cin >> a[i];

    sort(all(a));
    lli ans=0 ,j=1;
    while(j<=n and a[j]==a[1] ) j++;
    // disp(a)
    // cout << j << endl;
    if(j >n) { cout << 0 << endl; return; }

    for(int i=1 ;i<=n ;i++){
        while( j<=n and a[j] == a[i] ) j++;
        if( j<=n and a[i] < a[j] ) j++,ans++;
    }
    cout << ans <<  endl;
    

}




