void solve(){
    int n,m,q;
    string s,t;
    cin >> n >> m >> q >> s >> t;
    s = '0'+s;

    int pre[n+1] = {0};
    for(int i=1 ;i<=n ;i++){
        pre[i] = pre[i-1];
        if( i <= n-m+1 ){
            if( s.substr(i ,m) == t ) pre[i]++;
        }
    }
    // disp(pre)

    while(q -- ){
        int l,r;
        cin  >> l >> r;
        cout << max(0 ,pre[max(0 ,r-m+1)] - pre[l-1]) << endl;
    }

}




