void solve(){
    int n,m,k;
    cin >> n >> m >> k;

    vector<string> v(n+1);
    for(int i=1; i<=n ;i++){
        cin >> v[i];
        v[i] = '0' +v[i];
    }

    int ans=0;
    for(int i=1 ;i<=n ; i++){
        int j=1;
        while(j<=m){
            int c=0;
            if( v[i][j] == '.' ) c++;
            while( j+1<=m and v[i][j+1]!='*' ) c++,j++;
            ans += max(0 ,c-k+1); 
            j++;
        }
    }

    for(int i=1 ;i<=m ; i++){
        int j=1;
        while(j<=n){
            int c=0;
            if( v[j][i] == '.' ) c++;
            while( j+1<=n and v[j+1][i]!='*' ) c++,j++;
            ans += max(0 ,c-k+1); 
            j++;
        }
    }

    if(k==1) ans/=2;
    cout << ans << endl;

    

}




