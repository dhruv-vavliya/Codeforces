int ans = INT_MAX;
string out;

int cost( string s ,string pat ){
    int c=0;
    for( int i=0 ;i<s.size() ;i++ ){
        if( s[i] != pat[i%3] ){
            s[i] = pat[i%3];
            c++;
        }
    }
    if(c < ans) out = s ,ans=c;
    return c;
} 




