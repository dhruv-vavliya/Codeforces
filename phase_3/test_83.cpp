void solve(){
    lli n,m,k;
    cin >> n >> m >> k;

    if( k <= n-1 ){
        cout  << k+1  << ' ' << 1 << endl;
        return;
    }

    k -= (n-1);
    lli temp = k/(m-1);

    if( k%(m-1)  == 0 ){
        if( temp&1 ) cout << n-temp+1 << ' '  << m << endl;
        else cout << n-temp+1 << ' ' << 2 << endl;
    }
    else{
        if(temp&1){
            cout << n-temp  << ' ' << m-( k%(m-1) ) +1 << endl;
        }else{
            cout << n-temp << ' ' << 2 + (k%(m-1)) - 1 << endl;
        }
    }








    

}




